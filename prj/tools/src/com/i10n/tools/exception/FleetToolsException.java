package com.i10n.tools.exception;

public class FleetToolsException extends Exception {

    /**
	 * 
	 */
    private static final long serialVersionUID = 5384160507197259230L;

    public FleetToolsException() {
        super();
    }

    public FleetToolsException(Throwable cause) {
        super(cause);
    }

    public FleetToolsException(String msg, Throwable cause) {
        super(msg, cause);
    }

    public FleetToolsException(String msg) {
        super(msg);
    }
}
